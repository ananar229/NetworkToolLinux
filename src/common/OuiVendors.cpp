QString lookup(const QString &macAddress) {
    QString hex;
    for (QChar c : macAddress) {
        if (c.isLetterOrNumber()) {
            hex.append(c.toUpper());
        }
    }
    if (hex.size() < 6) {
        return QString();
    }
    bool ok = false;
    const quint32 prefix = hex.left(6).toUInt(&ok, 16);
    if (!ok) {
        return QString();
    }

    int lo = 0, hi = kOuiTableSize - 1;
    while (lo <= hi) {
        const int mid = (lo + hi) / 2;
        if (kOuiTable[mid].prefix == prefix) {
            return QString::fromUtf8(kOuiTable[mid].vendor);
        }
        if (kOuiTable[mid].prefix < prefix) {
            lo = mid + 1;
        } else {
            hi = mid - 1;
        }
    }
    return QString();
}

